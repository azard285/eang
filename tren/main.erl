-module(main).
-export([fact/1, fib/1, sum_list/1, len/1, rever/1]).

fact(1) ->
    1;

fact(N) ->
    N * fact(N-1).

%--------------------------------------------------------------------

fib(1) ->
    0;

fib(2) ->
    1;  

fib(N) ->
    fib(N-1) + fib(N-2).

%--------------------------------------------------------------------

sum_list([]) ->
    0;

sum_list(List) ->
    hd(List) + sum_list(tl(List)).

%--------------------------------------------------------------------

len([]) ->
    0;

len(List) ->
    1 + len(tl(List)).

%--------------------------------------------------------------------

rever([H | T]) ->
    if
        T == [] ->
            [H];
        true ->
            rever(T) ++ [H]
    end.




